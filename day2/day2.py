name = "Bijoy"
age = 25
country = "Bangladesh"
print(name) # this will print the value of the variable name
print(age) # this will print the value of the variable age 
print(country) # this will print the value of the variable country
print("My name is " + name) # this will print a string with the value of the variable name
print("I am " + str(age) + " years old") # this will print a string with the value of the variable age, we need to convert age to a string using str() function
print("I live in " + country) # this will print a string with the value of the variable country
print("Welcome", name) # this will print a string with the value of the variable name, we can use comma to separate the string and the variable
print("Welcome " + name) # this will print a string with the value of the variable name, we can use + to concatenate the string and the variable
print("Welcome {}".format(name)) # this will print a string with the value of the variable name, we can use format() method to format the string and the variable
print(f"Welcome {name}") # this will print a string with the value of the variable  name, we can use f-string to format the string and the variable